#include "pwd.h"

int    ft_pwd()
{
    char    *cwd;
        
    cwd = getcwd(NULL, 0);
    if (cwd == NULL)
        cwd = get_system_env("PWD");
    printf("%s\n", cwd);
    free(cwd);
    return (0);
}